        # 如果窗口已存在，不再创建
        if self.window is not None and self.window.winfo_exists():
            self.window.lift()
            return
        
        # 创建导出窗口
        self.window = tk.Toplevel(self.parent)
        self.window.title(get_text("export_requirements"))
        self.window.resizable(False, False)
        
        # 设置为模态窗口
        self.window.transient(self.parent)
        self.window.grab_set()
        
        # 创建主框架
        main_frame = ttk.Frame(self.window, padding=20)
        main_frame.pack(fill="both", expand=True)
        
        # 文件选择框架
        file_frame = ttk.Frame(main_frame)
        file_frame.pack(fill="x", pady=(0, 20))
        
        # 文件路径
        self.file_path_var = tk.StringVar()
        ttk.Label(file_frame, text=get_text("save_as")).pack(side="left", padx=(0, 5))
        ttk.Entry(file_frame, textvariable=self.file_path_var, width=50).pack(side="left", padx=(0, 5), fill="x", expand=True)
        ttk.Button(
            file_frame, 
            text=get_text("browse"),
            command=self.browse_file
        ).pack(side="left")
        
        # Python版本信息
        version = get_python_version(self.python_path)
